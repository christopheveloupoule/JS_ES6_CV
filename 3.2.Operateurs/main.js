"use strict"
// Opérateurs de comparaison
console.log(3 == 3); // true (égalité simple)
console.log(3 != 3); // false (non-égalité simple)
console.log(3 < 3); // false
console.log(3 <= 3); // true
// Egalité stricte
console.log(3 == "3"); // true
console.log(3 === "3"); // false
// Opérateurs logiques
console.log(2 < 3 && 3 > 4); // false (ET)
console.log(2 < 3 || 3 > 4); // true (OU)
