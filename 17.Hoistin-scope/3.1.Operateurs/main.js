"use strict"
// Opérateur d'affectation
let n = 5;
// Opérateurs arithmétiques
n = 1 + 3 * 4 - 6 / 2;  // 1 + 12 - 3 = 10
n = ((1 + 3) * 4 - 6) / 2;  // (16 - 6) / 2 = 5
n = 17 % 5; // 2 parce que 27 = 5*3 + 2
n = 2 ** 3; // 8
// Concaténation
let s = "Bon" + "jour"; // Bonjour
// Opérateurs combinés
n = 1;
n += 2; // n = n + 2;
// Incrémentation / décrémentation
n++; // n += 1; n = n + 1;
n--; // n -= 1; n = n - 1;
// Post / pré-incrémentation / décrémentation
